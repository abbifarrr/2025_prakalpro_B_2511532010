public class tugasAlproPekan2 {

	public static void main(String[] args) {
        // Mendeklarasikan dan menginisialisasi variabel untuk data barang
        int Tinggi = 155;
        float Berat = 101.5f; // Gunakan 'f' untuk menandakan tipe float
        char StatusObesitas = '+';
        boolean obesitas = true;

        // Menampilkan output data barang ke konsol
        System.out.println("=== Data Pasien ===");
        System.out.println("\nTinggi Badan    : " + Tinggi);
        System.out.println("\nBerat Badan     : " + Berat);
        System.out.println("\nStatus Obesitas : " + StatusObesitas);
        System.out.println("\nObesitas        : " + obesitas);
    }
}
